# 주어진 일본어 단어를 한국어 음소 및 숫자로 변환하는 함수
def japanese_to_korean(word):
    korean_word = ""
    for char in word:
        if char in japanese_korean_rules:
            korean_word += japanese_korean_rules[char]
    return korean_word

